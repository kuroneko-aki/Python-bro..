number = 3000

print("The number is {:.2f}".format(number)) # this will display only the first two digits after the decimal
print("The number is {:,}".format(number)) # will add a comma
print("The number is {:b}".format(number)) # will display a binary representation of the number
print("The number is {:o}".format(number)) # will be displayed as an octal number
print("The number is {:X}".format(number)) # will display the hexadecimal\
print("The number is {:E}".format(number)) # will display in scientific notation